    public void mostar3() throws SQLException {
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("Id");
        tb.addColumn("Constructora");
        tb.addColumn("Banco Vinculado");
        tabla.setModel(tb);
        String[] datos = new String[3];
        Connection con = Conexion.getConnection();
        PreparedStatement ps;
        ResultSet rs;
        String query = "SELECT ID_Compra,Constructora,Banco_Vinculado FROM Compra "
                + "INNER JOIN Proyecto ON Compra.ID_Proyecto = Proyecto.ID_Proyecto "
                + "WHERE Proveedor like 'Homecenter' AND Ciudad like 'Salento'";
        try {
            ps = con.prepareStatement(query);
            rs = ps.executeQuery();
            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                tb.addRow(datos);
            }
            tabla.setModel(tb);
        } catch (SQLException e) {
            System.out.println(e);
        }
    }

